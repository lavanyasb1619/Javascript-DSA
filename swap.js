// Swap by third variable 

let a = 10; 
let b = 20; 
console.log(a,b); 
let temp =0; 
temp = a; 
a=b
b=temp; 
console.log(a,b); 
//Swap without third variable 
let c=40; 
let d=50; 
console.log(c,d); 
[c,d] = [d,c]; 
console.log(c,d); 

// Arthematic way 
